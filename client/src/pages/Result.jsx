import { useLocation, useParams, useNavigate } from "react-router-dom";
import { useEffect, useState, useRef } from "react";
import { getItineraryById } from "../api/itinerary";
import jsPDF from "jspdf";
import "./Result.css";

export default function Result() {
  const { id } = useParams();
  const { state } = useLocation();
  const navigate = useNavigate();
  const [data, setData] = useState(state || null);
  const [loading, setLoading] = useState(!state);
  const [copied, setCopied] = useState(false);
  const [exporting, setExporting] = useState(false);
  const printRef = useRef();

  useEffect(() => {
    if (!state) {
      getItineraryById(id)
        .then((res) => setData(res.data.data))
        .catch(() => navigate("/"))
        .finally(() => setLoading(false));
    }
  }, [id, state, navigate]);

  const handleCopyLink = () => {
    navigator.clipboard.writeText(window.location.href);
    setCopied(true);
    setTimeout(() => setCopied(false), 2500);
  };

  const handleExportPDF = async () => {
    setExporting(true);
    try {
      const pdf = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
      const pageW = 210;
      const margin = 16;
      const contentW = pageW - margin * 2;
      let y = 20;

      const addPage = () => {
        pdf.addPage();
        y = 20;
      };

      const checkSpace = (needed) => {
        if (y + needed > 275) addPage();
      };

      // Header
      pdf.setFillColor(193, 68, 14);
      pdf.rect(0, 0, 210, 28, "F");
      pdf.setTextColor(255, 255, 255);
      pdf.setFont("helvetica", "bold");
      pdf.setFontSize(20);
      pdf.text("MysTrip", margin, 13);
      pdf.setFontSize(10);
      pdf.setFont("helvetica", "normal");
      pdf.text("AI Travel Itinerary", margin, 21);

      // Destination title
      y = 40;
      pdf.setTextColor(26, 10, 0);
      pdf.setFont("helvetica", "bold");
      pdf.setFontSize(18);
      pdf.text(`${data.destination}, Rajasthan`, margin, y);
      y += 8;

      // Tags row
      pdf.setFontSize(9);
      pdf.setFont("helvetica", "normal");
      pdf.setTextColor(140, 110, 93);
      const budgetLabel = { budget: "Budget", mid: "Mid-Range", premium: "Premium" };
      pdf.text(`${data.days} Days  •  ${budgetLabel[data.budget]}  •  ${data.interests?.join(", ")}`, margin, y);
      y += 6;

      // Divider
      pdf.setDrawColor(193, 68, 14);
      pdf.setLineWidth(0.5);
      pdf.line(margin, y, pageW - margin, y);
      y += 10;

      // Days
      data.itinerary?.forEach((day) => {
        checkSpace(20);

        // Day header
        pdf.setFillColor(255, 240, 232);
        pdf.roundedRect(margin, y - 5, contentW, 12, 2, 2, "F");
        pdf.setTextColor(193, 68, 14);
        pdf.setFont("helvetica", "bold");
        pdf.setFontSize(11);
        pdf.text(`Day ${day.day} — ${day.title}`, margin + 3, y + 3);
        y += 14;

        day.places?.forEach((place) => {
          checkSpace(30);

          // Place name
          pdf.setTextColor(26, 10, 0);
          pdf.setFont("helvetica", "bold");
          pdf.setFontSize(10);
          pdf.text(`${place.name}`, margin + 3, y);
          y += 5;

          // Time
          pdf.setTextColor(140, 110, 93);
          pdf.setFont("helvetica", "italic");
          pdf.setFontSize(8.5);
          pdf.text(`${place.time}`, margin + 3, y);
          y += 5;

          // Description
          pdf.setTextColor(74, 55, 40);
          pdf.setFont("helvetica", "normal");
          pdf.setFontSize(9);
          const descLines = pdf.splitTextToSize(place.description, contentW - 6);
          checkSpace(descLines.length * 4.5 + 10);
          pdf.text(descLines, margin + 3, y);
          y += descLines.length * 4.5 + 2;

          // Tip
          pdf.setFillColor(255, 248, 242);
          const tipLines = pdf.splitTextToSize(`💡 ${place.tip}`, contentW - 12);
          const tipH = tipLines.length * 4.5 + 4;
          checkSpace(tipH + 4);
          pdf.roundedRect(margin + 3, y - 2, contentW - 6, tipH, 1.5, 1.5, "F");
          pdf.setTextColor(193, 68, 14);
          pdf.setFontSize(8.5);
          pdf.text(tipLines, margin + 6, y + 2);
          y += tipH + 6;
        });

        y += 4;
      });

      // Footer
      pdf.setFontSize(7.5);
      pdf.setTextColor(180, 150, 130);
      pdf.setFont("helvetica", "italic");
      pdf.text("Generated by MysTrip AI • mystrip.in", margin, 290);

      pdf.save(`MysTrip-${data.destination}-${data.days}days.pdf`);
    } catch (err) {
      console.error("PDF export error:", err);
    } finally {
      setExporting(false);
    }
  };

  if (loading) return <div className="loading-screen">Loading your itinerary...</div>;
  if (!data) return null;

  const budgetLabel = { budget: "🎒 Budget", mid: "✈️ Mid-Range", premium: "👑 Premium" };

  return (
    <div className="result">
      <nav className="navbar">
        <span className="logo" onClick={() => navigate("/")} style={{ cursor: "pointer" }}>🗺️ MysTrip</span>
        <div className="nav-right">
          <a href="/history">Past Itineraries</a>
          <button className="new-btn" onClick={() => navigate("/")}>+ New Trip</button>
        </div>
      </nav>

      <div className="result-hero">
        <h1>📍 {data.destination}, Rajasthan</h1>
        <div className="tags">
          <span className="tag">{data.days} Days</span>
          <span className="tag">{budgetLabel[data.budget]}</span>
          {data.interests?.map((i) => (
            <span key={i} className="tag interest">{i}</span>
          ))}
        </div>

        {/* ACTION BUTTONS */}
        <div className="action-bar">
          <button className="action-btn" onClick={handleCopyLink}>
            {copied ? "✅ Link Copied!" : "🔗 Share Link"}
          </button>
          <button className="action-btn primary" onClick={handleExportPDF} disabled={exporting}>
            {exporting ? "⏳ Exporting..." : "📄 Export PDF"}
          </button>
        </div>
      </div>

      <div className="itinerary-list" ref={printRef}>
        {data.itinerary?.map((day) => (
          <div key={day.day} className="day-card">
            <div className="day-header">
              <span className="day-num">Day {day.day}</span>
              <h2>{day.title}</h2>
            </div>
            <div className="places">
              {day.places?.map((place, idx) => (
                <div key={idx} className="place-item">
                  <div className="place-meta">
                    <span className="place-time">⏰ {place.time}</span>
                  </div>
                  <h3>{place.name}</h3>
                  <p className="place-desc">{place.description}</p>
                  <div className="place-tip">💡 {place.tip}</div>
                </div>
              ))}
            </div>
          </div>
        ))}
      </div>

      <div className="result-footer">
        <small>Generated by Travel Itinerary AI • Saved to database • ID: {id}</small>
      </div>
    </div>
  );
}
